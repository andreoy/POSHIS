    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try {
            Kasir kasir_input = new Kasir(txt_kasir.getText());
            Kasir input=km.addKasir(kasir_input);
            
            modelKasir.addRow(new Object[]{input.getId_kasir(), input.getKasir()});
            Item item = new Item(input.getId_kasir(),input.getKasir());
            cb_kasir.addItem(item);
            
            Sessi sesi = new Sessi();
            sesi.setStatus(0);
            
            sm.addSessi(sesi);
            
        } catch (SQLException ex) {
            Logger.getLogger(formSessi.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_simpanActionPerformed

