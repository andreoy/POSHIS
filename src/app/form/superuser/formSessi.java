 private DefaultTableModel setToTable(){
        Object[][] rowData = new Object[0][7];
        
        
         Object column[] = {"ID ", "ID KASIR", "STATUS", "BUKA", "SALDO", "TUTUP", "SALDO"};

        DefaultTableModel jtable = new DefaultTableModel(rowData, column);

        return jtable;
  
    }    
private void setTableData(ArrayList<Sessi> sessis) throws SQLException, ParseException {
        
        modelSesi.setRowCount(0);
        for (int i = 0; i < sessis.size(); i++) {
            
           
            modelSesi.addRow(new Object[]{
                sessis.get(i).getId_sesi(),
                km.getDataById(sessis.get(i).getId_kasir()).get(0).getKasir(),
                sessis.get(i).getStatus(),
                sessis.get(i).getTime_open_sesi(),
                sessis.get(i).getSaldo_awal(),
                sessis.get(i).getTime_close_sesi(),
                sessis.get(i).getSaldo_akhir()
                
            });

        }

    }

    void clearForm(){
        txt_status.setText("");
        txt_saldo_akhir.setText("");
        txt_saldo_awal.setText("");
        btn_buka_kasir.setEnabled(false);
        btn_tutup_kasir.setEnabled(false);
    }
    
    void checkStatus(){
        try {
            Item item = (Item) cb_kasir.getSelectedItem();
            ArrayList<Sessi> sesi_selected=sm.getDataByIdKasir(item.getId());
             
            this.setTableData(sesi_selected);
            
            
            if(sm.getStatus(item.getId())!= null){
                sesiTerakhir= sm.getStatus(item.getId());
                txt_status.setText(sesiTerakhir.getStatus());
                
                this.setTableData(sesi_selected);
                if(txt_status.getText().equals("Open")) {
                    btn_buka_kasir.setEnabled(false);
                    btn_tutup_kasir.setEnabled(true);
                    txt_saldo_awal.setEnabled(false);
                    txt_saldo_awal.setText(sesiTerakhir.getSaldo_awal()+"");
                    
                    double saldo=pm.getJumlahBySesi(sesiTerakhir.getId_sesi());
                    double total = sesiTerakhir.getSaldo_awal()+saldo;
                    txt_saldo_akhir.setText(total+"");
                    
                }else{
                    btn_buka_kasir.setEnabled(true);
                    btn_tutup_kasir.setEnabled(false);
                    txt_saldo_awal.setEnabled(true);
                    txt_saldo_awal.setText("");
                    txt_saldo_akhir.setText("");
                }
            }
            else{
                txt_status.setText("Close");
                btn_buka_kasir.setEnabled(true);
                btn_tutup_kasir.setEnabled(false);
                txt_saldo_awal.setEnabled(true);
                txt_saldo_awal.setText("");
            }
            
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(formSessi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
